public class Stringsss {
    public static void main(String[] args) {
        String a = "Hello";
        String b = "World";
        //adding two strings
        String c = a + b;
        System.out.println(c);
        //finding character
        System.out.println(a.charAt(2));
        //finding length of string
        System.out.println(b.length());
    }
}
